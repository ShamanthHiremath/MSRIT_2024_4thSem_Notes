# Description: Arithmetic operations in R
vec1 <- c(1,2,3,4,5)
vec2 <- c(6,7,8,9,10)

# Addition
addition <- vec1 + vec2
cat("Addition of vec1 and vec2 is:", addition, "\n")

# Subtraction
subtraction <- vec1 - vec2
cat("Subtraction of vec1 and vec2 is:", subtraction, "\n")

# Multiplication
multiplication <- vec1 * vec2
cat("Multiplication of vec1 and vec2 is:", multiplication, "\n")

# Division
division <- vec1 / vec2
cat("Division of vec1 and vec2 is:", division, "\n")

# Modulus
modulus <- vec1 %% vec2
cat("Modulus of vec1 and vec2 is:", modulus, "\n")

# Exponentiation
exponentiation <- vec1 ^ vec2
cat("Exponentiation of vec1 and vec2 is:", exponentiation, "\n")